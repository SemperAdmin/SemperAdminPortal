/**
 * Returns the MarinesCategory entries belonging to a parent group, in the
 * order specified by the group's children array.
 */
export function getCategoriesForGroup(groupSlug: string): MarinesCategory[] {
  const group = findMarinesParentGroup(groupSlug);
  if (!group) return [];
  return group.children
    .map((s) => findMarinesCategory(s))
    .filter((c): c is MarinesCategory => c !== undefined);
}

/**
 * Returns the MarinesCategory entries that are not consolidated under
 * any parent group. The main /marines page renders these as individual
 * cards alongside the parent-group cards.
 */
export function getUngroupedCategories(): MarinesCategory[] {
  const groupedSlugs = new Set(
    MARINES_PARENT_GROUPS.flatMap((g) => g.children)
  );
  return MARINES_CATEGORIES.filter((c) => !groupedSlugs.has(c.slug));
}

/**
 * Returns the parent group a given category belongs to, or undefined if
 * the category is not consolidated under any parent group. Used by the
 * breadcrumb on leaf-topic and grouped-topic pages to show the parent
 * group as the second segment (e.g., Marines > Pay and Entitlements > Basic Pay).
 */
export function findParentGroupForCategory(
  categorySlug: string
): MarinesParentGroup | undefined {
  return MARINES_PARENT_GROUPS.find((g) => g.children.includes(categorySlug));
}
