/**
 * Marines tree category definitions.
 * Used by /marines landing page (alphabetical 21-card grid) and category indexes.
 *
 * The slug field becomes the URL segment: /marines/[slug]/[page-slug]
 * Existing MDX files use the slug as their `topic` field value.
 */
export interface MarinesCategory {
  slug: string;
  label: string;
  shortLabel: string;
  description: string;
  /** Lucide icon name. */
  icon:
    | "Trophy"
    | "Home"
    | "Utensils"
    | "DollarSign"
    | "Plane"
    | "Users"
    | "FileText"
    | "Heart"
    | "LifeBuoy"
    | "LogIn"
    | "CalendarDays"
    | "Heart2"
    | "AlertCircle"
    | "Truck"
    | "TrendingUp"
    | "ClipboardList"
    | "RefreshCcw"
    | "LogOut"
    | "Star"
    | "Briefcase"
    | "Shirt"
    | "GraduationCap"
    | "CreditCard";
  /**
   * "container": the topic has multiple sub-pages. /marines/[slug] renders an
   *   index of those sub-pages, and each sub-page lives at /marines/[slug]/[page-slug].
   * "leaf": the topic is a single wiki page. /marines/[slug] renders the
   *   single MDX file (whose slug equals the topic slug). No sub-pages.
   *
   * Defaults to "container" if unset.
   */
  pageType?: "container" | "leaf";
}

